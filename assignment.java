public class assignment {
    public static void main(String[] args) {
        int p=10;
        int q;

        //use of =
        q=p;
        System.out.println(q);//10

        // use of +=
        p+=q;
        System.out.println(p);//20

        //use of -=
        p-=q;
        System.out.println(p);//10

        // use of / 
        p/=q;
        System.out.println(p);

        // use of *
        p*=q;
        System.out.println(p);


    }
    
}
